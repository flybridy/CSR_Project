package com.fleety.analysis.track.task;

public class DestInfo {

	private String destNo="";
	
	private int mdtId=0;
	
	private int companyId=0;
	
	private int runComId=0;
	
	private String companyName="";
	
	private String runComName="";

	public String getDestNo() {
		return destNo;
	}

	public void setDestNo(String destNo) {
		this.destNo = destNo;
	}

	public int getMdtId() {
		return mdtId;
	}

	public void setMdtId(int mdtId) {
		this.mdtId = mdtId;
	}

	public int getCompanyId() {
		return companyId;
	}

	public void setCompanyId(int companyId) {
		this.companyId = companyId;
	}

	public int getRunComId() {
		return runComId;
	}

	public void setRunComId(int runComId) {
		this.runComId = runComId;
	}

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	public String getRunComName() {
		return runComName;
	}

	public void setRunComName(String runComName) {
		this.runComName = runComName;
	}
	
}
